export function drawSprite(ctx, sprite, palette, x, y, scale = 3) {
  for (let j = 0; j < sprite.length; j += 1) {
    const row = sprite[j];
    for (let i = 0; i < row.length; i += 1) {
      const key = row[i];
      if (key === '0') continue;
      ctx.fillStyle = palette[key] ?? '#ff00ff';
      ctx.fillRect(Math.floor(x + i * scale), Math.floor(y + j * scale), scale, scale);
    }
  }
}
